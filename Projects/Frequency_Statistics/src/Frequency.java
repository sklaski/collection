
public class Frequency {

	private String string;
	private int count;
	
	public Frequency(String string, int count) {
		super();
		this.string = string;
		this.count = count;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getString() {
		return string;
	}
	
}
